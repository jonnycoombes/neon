// ReSharper disable All
namespace JCS.Neon.Glow.Utilities.Console
{
    /// <summary>
    ///     Static class that just contains the most commonly used ASCII codes (or low Unicode ones)
    /// </summary>
    public static class AsciiCodes
    {
        /// <summary>
        ///     Null
        /// </summary>
        public const char NUL = '\x00';

        /// <summary>
        ///     Start of Heading
        /// </summary>
        public const char SOH = '\x01';

        /// <summary>
        ///     End of Text
        /// </summary>
        public const char ETX = '\x03';

        /// <summary>
        ///     End of Transmission
        /// </summary>
        public const char EOT = '\x04';

        /// <summary>
        ///     Enquiry
        /// </summary>
        public const char ENQ = '\x05';

        /// <summary>
        ///     Acknowledge
        /// </summary>
        public const char ACK = '\x06';

        /// <summary>
        ///     Bell
        /// </summary>
        public const char BEL = '\x07';

        /// <summary>
        ///     Backspace
        /// </summary>
        public const char BS = '\x08';

        /// <summary>
        ///     Tabulate
        /// </summary>
        public const char TAB = '\x09';

        /// <summary>
        ///     Line Feed
        /// </summary>
        public const char LF = '\x0a';

        /// <summary>
        ///     Vertical Tabulation
        /// </summary>
        public const char VT = '\x0b';

        /// <summary>
        ///     Form Feed
        /// </summary>
        public const char FF = '\x0c';

        /// <summary>
        ///     Carriage Return
        /// </summary>
        public const char CR = '\x0d';

        /// <summary>
        ///     Shift Out
        /// </summary>
        public const char SO = '\x0e';

        /// <summary>
        ///     Shift In
        /// </summary>
        public const char SI = '\x0f';

        /// <summary>
        ///     Data Link Escape
        /// </summary>
        public const char DLE = '\x10';

        /// <summary>
        ///     Device Control 1 (XON)
        /// </summary>
        public const char DC1 = '\x11';

        /// <summary>
        ///     Device Control 2
        /// </summary>
        public const char DC2 = '\x12';

        /// <summary>
        ///     Device Control 3 (XOFF)
        /// </summary>
        public const char DC3 = '\x13';

        /// <summary>
        ///     Device Control 4
        /// </summary>
        public const char DC4 = '\x14';

        /// <summary>
        ///     Negative Acknowledge
        /// </summary>
        public const char NAK = '\x15';

        /// <summary>
        ///     Synchronous Idle
        /// </summary>
        public const char SYN = '\x16';

        /// <summary>
        ///     End of Transmission Block
        /// </summary>
        public const char ETB = '\x17';

        /// <summary>
        ///     Cancel
        /// </summary>
        public const char CAN = '\x18';

        /// <summary>
        ///     End of Medium
        /// </summary>
        public const char EM = '\x19';

        /// <summary>
        ///     Substitute
        /// </summary>
        public const char SUB = '\x1a';

        /// <summary>
        ///     Escape
        /// </summary>
        public const char ESC = '\x1b';

        /// <summary>
        ///     File Separator
        /// </summary>
        public const char FS = '\x1c';

        /// <summary>
        ///     Group Separator
        /// </summary>
        public const char GS = '\x1d';

        /// <summary>
        ///     Record Separator
        /// </summary>
        public const char RS = '\x1e';

        /// <summary>
        ///     Unit Separator
        /// </summary>
        public const char US = '\x1f';

        /// <summary>
        ///     Space
        /// </summary>
        public const char SPC = '\x20';

        /// <summary>
        ///     Delete
        /// </summary>
        public const char DEL = '\x7f';

        /// <summary>
        ///     Start of Text
        /// </summary>
        public const char STX = '\x02';
    }
}